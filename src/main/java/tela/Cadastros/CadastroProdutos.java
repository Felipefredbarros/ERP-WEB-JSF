    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        prod.setTipo(campoTipo.getText());
        prod.setNumeracao(campoNumeração.getText());
        prod.setCor(campoCor.getText());
        prod.setNcm(campoNCM.getText());
        prod.setCont(0d);
        String precoTextoVenda = campoValorVenda.getText();
        if (!precoTextoVenda.equals("")) {
            double precovendaDouble = Double.parseDouble(precoTextoVenda);
            BigDecimal salarioBigDecimal = new BigDecimal(precovendaDouble);
            prod.setValorUnitarioVenda(salarioBigDecimal);
        }

        prod.setEstoque(Double.parseDouble(campoEstoque.getText()));

        if (adicionarProd(prod)) {
            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().merge(prod);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();
            if (habilitarEdicao) {
                JOptionPane.showMessageDialog(null, "EDIÇÃO REALIZADA COM SUCESSO");
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "PRODUTO CADASTRADO COM SUCESSO");
            }
            limpaCampos();
            habilitarEdicao = false;
            prod = new Produto();
            validaCampos("inicio");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

