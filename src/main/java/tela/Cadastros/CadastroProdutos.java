    public void limpaCampos() {
        campoTipo.setText("");
        campoNCM.setText("");
        campoEstoque.setText("");
        campoCor.setText("");
        campoNumeração.setText("");
        campoValorVenda.setText("");
    }

    public void SetCampos(Produto prod) {
        campoTipo.setText(prod.getTipo());
        campoNumeração.setText(prod.getNumeracao());
        campoCor.setText(prod.getCor());
        campoNCM.setText(prod.getNcm());
        campoValorVenda.setText(prod.getValorUnitarioVenda().toString());
        campoEstoque.setText(prod.getEstoque().toString());
        this.prod.setId(prod.getId());

        validaCampos("novo");

    }

    public boolean adicionarProd(Produto prod) {
        if (prod.getTipo() == null || prod.getTipo().isEmpty()
                || prod.getNcm() == null || prod.getNcm().isEmpty()
                || prod.getEstoque() == null
                || prod.getCor() == null || prod.getCor().isEmpty()
                || prod.getNumeracao() == null || prod.getNumeracao().isEmpty()
                || prod.getValorUnitarioVenda() == null) {
            JOptionPane.showMessageDialog(null, "TODOS OS CAMPOS PRECISAM SER PREENCHIDOS");
            return false;
        }
        return true;
    }

    public void validaCampos(String operacao) {
        if (operacao.equals("inicio")) {
            btNovo.setEnabled(true);
            btSalvar.setEnabled(false);
            btCancelar.setEnabled(false);
            campoTipo.setEnabled(false);
            campoCor.setEnabled(false);
            campoEstoque.setEnabled(false);
            campoNCM.setEnabled(false);
            campoNumeração.setEnabled(false);
            campoValorVenda.setEnabled(false);
        } else if (operacao.equals("novo")) {
            btNovo.setEnabled(false);
            btSalvar.setEnabled(true);
            btCancelar.setEnabled(true);
            campoTipo.setEnabled(true);
            campoCor.setEnabled(true);
            campoEstoque.setEnabled(true);
            campoNCM.setEnabled(true);
            campoNumeração.setEnabled(true);
            campoValorVenda.setEnabled(true);
        } else if (operacao.equals("selecionado")) {
            btNovo.setEnabled(true);
            btSalvar.setEnabled(false);
            btCancelar.setEnabled(true);
            campoTipo.setEnabled(false);
            campoCor.setEnabled(false);
            campoEstoque.setEnabled(false);
            campoNCM.setEnabled(false);
            campoNumeração.setEnabled(false);
            campoValorVenda.setEnabled(false);
        }

    }

